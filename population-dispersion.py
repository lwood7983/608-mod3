# Measures of Dispersion example using 10 six-sided die rolls data

import statistics
statistics.pvariance([1, 3, 4, 2, 6, 5, 3, 4, 5, 2])
print('This is population variance using pvariance: ', statistics.pvariance([1, 3, 4, 2, 6, 5, 3, 4, 5, 2]))

import statistics
statistics.pstdev([1, 3, 4, 2, 6, 5, 3, 4, 5, 2])
print('This is population standard deviation using pstdev: ', statistics.pstdev([1, 3, 4, 2, 6, 5, 3, 4, 5, 2]))

